	Hrs[3] = -0.125*(1 - t);
	Hrs[4] = 0.125*(1 + t);
	Hrs[5] = -0.125*(1 + t);
	Hrs[6] = 0.125*(1 + t);
	Hrs[7] = -0.125*(1 + t);

