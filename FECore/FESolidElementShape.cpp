//-----------------------------------------------------------------------------
//! values of shape functions
void FEPenta15::shape_fnc(double* H, double r, double s, double t)
{
	double u = 1 - r - s;

	H[0] = -((1 - t*t)*u) / 2. + ((1 - t)*u*(-1 + 2 * u)) / 2.;
	H[1] = (r*(-1 + 2 * r)*(1 - t)) / 2. - (r*(1 - t*t)) / 2.;
	H[2] = (s*(-1 + 2 * s)*(1 - t)) / 2. - (s*(1 - t*t)) / 2.;
	H[3] = -((1 - t*t)*u) / 2. + ((1 + t)*u*(-1 + 2 * u)) / 2.;
	H[4] = (r*(-1 + 2 * r)*(1 + t)) / 2. - (r*(1 - t*t)) / 2.;
	H[5] = (s*(-1 + 2 * s)*(1 + t)) / 2. - (s*(1 - t*t)) / 2.;
	H[6] = 2 * r*(1 - t)*u;
	H[7] = 2 * r*s*(1 - t);
	H[8] = 2 * s*(1 - t)*u;
	H[9] = 2 * r*(1 + t)*u;
	H[10] = 2 * r*s*(1 + t);
	H[11] = 2 * s*(1 + t)*u;
	H[12] = (1 - t*t)*u;
	H[13] = r*(1 - t*t);
	H[14] = s*(1 - t*t);
}

//-----------------------------------------------------------------------------
//! values of shape function derivatives
void FEPenta15::shape_deriv(double* Hr, double* Hs, double* Ht, double r, double s, double t)
{
	Hr[0] = -((-1 + t)*(-2 + 4 * r + 4 * s + t)) / 2.;
	Hr[1] = (-2 - 4 * r*(-1 + t) + t + t*t) / 2.;
	Hr[2] = 0;
	Hr[3] = ((-2 + 4 * r + 4 * s - t)*(1 + t)) / 2.;
	Hr[4] = ((1 + t)*(-2 + 4 * r + t)) / 2.;
	Hr[5] = 0;
	Hr[6] = 2 * (-1 + 2 * r + s)*(-1 + t);
	Hr[7] = -2 * s*(-1 + t);
	Hr[8] = 2 * s*(-1 + t);
	Hr[9] = -2 * (-1 + 2 * r + s)*(1 + t);
	Hr[10] = 2 * s*(1 + t);
	Hr[11] = -2 * s*(1 + t);
	Hr[12] = -1 + t*t;
	Hr[13] = 1 - t*t;
	Hr[14] = 0;

	Hs[0] = -((-1 + t)*(-2 + 4 * r + 4 * s + t)) / 2.;
	Hs[1] = 0;
	Hs[2] = (-2 - 4 * s*(-1 + t) + t + t*t) / 2.;
	Hs[3] = ((-2 + 4 * r + 4 * s - t)*(1 + t)) / 2.;
	Hs[4] = 0;
	Hs[5] = ((1 + t)*(-2 + 4 * s + t)) / 2.;
	Hs[6] = 2 * r*(-1 + t);
	Hs[7] = -2 * r*(-1 + t);
	Hs[8] = 2 * (-1 + r + 2 * s)*(-1 + t);
	Hs[9] = -2 * r*(1 + t);
	Hs[10] = 2 * r*(1 + t);
	Hs[11] = -2 * (-1 + r + 2 * s)*(1 + t);
	Hs[12] = -1 + t*t;
	Hs[13] = 0;
	Hs[14] = 1 - t*t;

	Ht[0] = -((-1 + r + s)*(-1 + 2 * r + 2 * s + 2 * t)) / 2.;
	Ht[1] = (r*(1 - 2 * r + 2 * t)) / 2.;
	Ht[2] = (s*(1 - 2 * s + 2 * t)) / 2.;
	Ht[3] = ((-1 + r + s)*(-1 + 2 * r + 2 * s - 2 * t)) / 2.;
	Ht[4] = r*(-0.5 + r + t);
	Ht[5] = s*(-0.5 + s + t);
	Ht[6] = 2 * r*(-1 + r + s);
	Ht[7] = -2 * r*s;
	Ht[8] = 2 * s*(-1 + r + s);
	Ht[9] = -2 * r*(-1 + r + s);
	Ht[10] = 2 * r*s;
	Ht[11] = -2 * s*(-1 + r + s);
	Ht[12] = 2 * (-1 + r + s)*t;
	Ht[13] = -2 * r*t;
	Ht[14] = -2 * s*t;
}

